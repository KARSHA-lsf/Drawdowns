package lsf.drawdowns.dbCon;

public class Drawdown {
	int permno;
	int yrmo;
	double capm_resid;
	String date;
	
	public int getPermno() {
		return permno;
	}
	public void setPermno(int permno) {
		this.permno = permno;
	}
	public int getYrmo() {
		return yrmo;
	}
	public void setYrmo(int yrmo) {
		this.yrmo = yrmo;
	}
	public double getCapm_resid() {
		return capm_resid;
	}
	public void setCapm_resid(double capm_resid) {
		this.capm_resid = capm_resid;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	
	

}
